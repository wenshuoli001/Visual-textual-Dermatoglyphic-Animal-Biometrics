class build_clipmix(nn.Module):
    def __init__(self, num_classes, camera_num, view_num, cfg):
        super().__init__()
        self.num_classes = num_classes
        self.in_planes = 512
        self.neck_feat = cfg.TEST.NECK_FEAT


        if cfg.MODEL.SIE_CAMERA:
            camera_num = camera_num
        else:
            camera_num = 0
        if cfg.MODEL.SIE_VIEW:
            view_num = view_num
        else:
            view_num = 0

        '''定义clipbackbone '''
        self.base_model, base_cfg = build_CLIP_from_openai_pretrained(cfg.MODEL.CLIPMIX_pretrain_choice, cfg.MODEL.CLIPMIX_img_size,
                                                                      cfg.MODEL.CLIPMIX_stride_size)

        self.embed_dim = base_cfg['embed_dim']  # 从 base_cfg 字典中获取 'embed_dim' 的值，并赋值给类的实例变量 self.embed_dim。

        '''创建交叉注意力和交叉transformer，用于构建融合模块'''
        self.cross_attn = nn.MultiheadAttention(self.embed_dim,
                                                self.embed_dim // 64,
                                                batch_first=True)  # 创建一个多头注意力层，并赋值给类的实例变量 self.cross_attn。
        self.cross_modal_transformer = Transformer(width=self.embed_dim,
                                                   layers=2,
                                                   heads=self.embed_dim //
                                                         64)  # 创建一个 Transformer 模型，并赋值给类的实例变量 self.cross_modal_transformer
        scale = self.cross_modal_transformer.width ** -0.5  # 计算 scale，赋值给 scale。

        self.ln_pre_t = LayerNorm(self.embed_dim)  # 创建一个 LayerNorm 层，并赋值给类的实例变量 self.ln_pre_t。
        self.ln_pre_i = LayerNorm(self.embed_dim)  # 创建一个 LayerNorm 层，并赋值给类的实例变量 self.ln_pre_i。
        self.ln_post = LayerNorm(self.embed_dim)  # 创建一个 LayerNorm 层，并赋值给类的实例变量 self.ln_post。

        proj_std = scale * ((2 * self.cross_modal_transformer.layers) ** -0.5)  # 计算 proj_std，赋值给 proj_std。
        attn_std = scale  # 赋值给 attn_std。
        fc_std = (2 * self.cross_modal_transformer.width) ** -0.5  # 赋值给 fc_std。
        for block in self.cross_modal_transformer.resblocks:  # 遍历 self.cross_modal_transformer.resblocks，执行下面的代码
            nn.init.normal_(block.attn.in_proj_weight,
                            std=attn_std)  # 对 block.attn.in_proj_weight 进行初始化，初始化方法为正态分布，均值为 0，标准差为 attn_std。
            nn.init.normal_(block.attn.out_proj.weight,
                            std=proj_std)  # 对 block.attn.out_proj.weight 进行初始化，初始化方法为正态分布，均值为 0，标准差为 proj_std。
            nn.init.normal_(block.mlp.c_fc.weight,
                            std=fc_std)  # 对 block.mlp.c_fc.weight 进行初始化，初始化方法为正态分布，均值为 0，标准差为 fc_std。
            nn.init.normal_(block.mlp.c_proj.weight,
                            std=proj_std)  # 对 block.mlp.c_proj.weight 进行初始化，初始化方法为正态分布，均值为 0，标准差为 proj_std。

        # init cross attn
        nn.init.normal_(self.cross_attn.in_proj_weight,
                        std=attn_std)  # 对 self.cross_attn.in_proj_weight 进行初始化，初始化方法为正态分布，均值为 0，标准差为 attn_std。
        nn.init.normal_(self.cross_attn.out_proj.weight,
                        std=proj_std)  # 对 self.cross_attn.out_proj.weight 进行初始化，初始化方法为正态分布，均值为 0，标准差为 proj_std。


        '''构建用于不同loss的分类头'''
        self.ID_LOSS_TYPE = cfg.MODEL.ID_LOSS_TYPE
        if self.ID_LOSS_TYPE == 'arcface':
            print('using {} with s:{}, m: {}'.format(self.ID_LOSS_TYPE, cfg.SOLVER.COSINE_SCALE,
                                                     cfg.SOLVER.COSINE_MARGIN))
            self.classifier = Arcface(self.in_planes, self.num_classes,
                                      s=cfg.SOLVER.COSINE_SCALE, m=cfg.SOLVER.COSINE_MARGIN)
        elif self.ID_LOSS_TYPE == 'cosface':
            print('using {} with s:{}, m: {}'.format(self.ID_LOSS_TYPE, cfg.SOLVER.COSINE_SCALE,
                                                     cfg.SOLVER.COSINE_MARGIN))
            self.classifier = Cosface(self.in_planes, self.num_classes,
                                      s=cfg.SOLVER.COSINE_SCALE, m=cfg.SOLVER.COSINE_MARGIN)
        elif self.ID_LOSS_TYPE == 'amsoftmax':
            print('using {} with s:{}, m: {}'.format(self.ID_LOSS_TYPE, cfg.SOLVER.COSINE_SCALE,
                                                     cfg.SOLVER.COSINE_MARGIN))
            self.classifier = AMSoftmax(self.in_planes, self.num_classes,
                                        s=cfg.SOLVER.COSINE_SCALE, m=cfg.SOLVER.COSINE_MARGIN)
        elif self.ID_LOSS_TYPE == 'circle':
            print('using {} with s:{}, m: {}'.format(self.ID_LOSS_TYPE, cfg.SOLVER.COSINE_SCALE,
                                                     cfg.SOLVER.COSINE_MARGIN))
            self.classifier = CircleLoss(self.in_planes, self.num_classes,
                                         s=cfg.SOLVER.COSINE_SCALE, m=cfg.SOLVER.COSINE_MARGIN)
        else:
            self.classifier = nn.Linear(self.in_planes, self.num_classes, bias=False)
            self.classifier.apply(weights_init_classifier)

        '''构建最后的bottleneck'''
        self.bottleneck = nn.BatchNorm1d(self.in_planes)
        self.bottleneck.bias.requires_grad_(False)
        self.bottleneck.apply(weights_init_kaiming)

    def cross_former(self, q, k, v):  # 定义 cross_former 方法，方法的参数为 q、k、v。
        '''
        构建融合模块，用于对文本和图像进行编码。
        '''
        x = self.cross_attn(
            self.ln_pre_t(q),
            self.ln_pre_i(k),
            self.ln_pre_i(v),
            need_weights=False)[0]  # 将qkv先进行LayerNorm，然后输入到self.cross_attn中，得到输出，赋值给x。
        #打印x的维度
        #print('x',x.shape)
        #x = x.permute(1, 0, 2)  # NLD -> LND#将x的维度进行转换，转换后的维度为LND，赋值给x。这么做的目的是为了方便后面的Transformer。
        x = self.cross_modal_transformer(x)  # 将x输入到self.cross_modal_transformer中，得到输出，赋值给x。
        #print('x', x.shape)
        #x = x.permute(1, 0, 2)  # LND -> NLD#将x的维度进行转换，转换后的维度为NLD，赋值给x。

        x = self.ln_post(x)  # 将x进行LayerNorm，赋值给x。
        return x

    def encode_image(self, image):
        '''
        调用clip的图像编码器
        '''
        x = self.base_model.encode_image(
            image)  # 调用了base_model的encode_image方法来,对输入的图像进行编码，并将结果存储在x中,这个方法在clip_model.py中定义。
        return x[:, 0,
               :].float()  # 返回x的一个子集，并将其转换为浮点数。[:, 0, :]表示选择所有的第一维，第二维的第一个元素，以及所有的第三维。只需要第二维的第一个元素，是因为第二维的第一个元素是图像的编码，而第二维的第二个元素是图像的位置编码。
        # return x.float() # for CLIP ResNet visual model

    def encode_text(self, text):
        '''
        调用clip的文本编码器
        '''
        x = self.base_model.encode_text(
            text)  # 调用了base_model的encode_text方法来,对输入的文本进行编码，并将结果存储在x中,这个方法在clip_model.py中定义。
        return x[torch.arange(x.shape[0]), text.argmax(
            dim=-1)].float()  # 返回x的一个子集，并将其转换为浮点数。这么做的目的是为了方便后面的分类器。torch.arange(x.shape[0])表示选择所有的第一维，text.argmax(dim=-1)表示选择text中最大值的索引。


